import numpy as np
from NR import NR


def f(x):                                                   
    y = np.zeros((3,1))                                      
    y[0] = (-1.0)*(x[1]**2.)-(x[2]**2.)-(1./4.*x[0])+2.
    y[1] = (x[0]*x[1])-(4.*x[0]*x[2])-x[1]+1.
    y[2] = (4.*x[0]*x[1])+(x[0]*x[2]) - x[2]
    return y

def DF(x):                                                   
    J = np.zeros((3,3))                                      
    J[0,0] = -1./4.
    J[0,1] = -2.*x[1]
    J[0,2] = -2.*x[2]
    J[1,0] = x[1]-4.*x[2]
    J[1,1] = x[0]-1.
    J[1,2] = -4.*x[0]
    J[2,0] = 4.*x[1]+x[2]
    J[2,1] = 4.*x[0]
    J[2,2] = x[0]-1
    return J

x0 = np.array([[7.0],[0.0],[0.0]]) 

x, err, res = NR(x0, f, DF, 0.00001, 0.00001, 15)